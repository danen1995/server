    private void jBtnIzaberiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnIzaberiActionPerformed
        int izabraniRed = jTblMesta.getSelectedRow();
        if (izabraniRed >= 0) {
            Long ptt = (Long) jTblMesta.getValueAt(izabraniRed, 0);
            JOptionPane.showMessageDialog(null, "Ptt izabranog mesta: " + ptt);

            try {
                MestoEntity mesto = Controller.pronadjiMestoPrekoPtta(ptt);
                Session.getInstance().getMap().put("izabrano_mesto", mesto);
                dispose();
            } catch (Exception ex) {
                Logger.getLogger(FIzaberiMesto.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Izaberi mesto!!!");
        }
    }//GEN-LAST:event_jBtnIzaberiActionPerformed


