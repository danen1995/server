    private void popuniTabeluMesta() {
        try {
            List<MestoEntity> mesta = Controller.ucitajListuMesta();

            TableModel tm = new MestoTableModel(mesta);
            jTblMesta.setModel(tm);
        } catch (Exception ex) {
            Logger.getLogger(FIzaberiMesto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void centrirajFormu() {
        setLocationRelativeTo(null);
    }

    private void pripremiTabeluZaSortiranje() {
        TableRowSorter<TableModel> rowSorter = new TableRowSorter<>(jTblMesta.getModel());
        jTblMesta.setRowSorter(rowSorter);

        jtxtPretragaNazivaMesta.getDocument().addDocumentListener(new DocumentListener() {

            @Override
            public void insertUpdate(javax.swing.event.DocumentEvent e) {
                String filterMesto = jtxtPretragaNazivaMesta.getText();

                if (filterMesto.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + filterMesto));
                }
            }

            @Override
            public void removeUpdate(javax.swing.event.DocumentEvent e) {
                String text = jtxtPretragaNazivaMesta.getText();

                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void changedUpdate(javax.swing.event.DocumentEvent e) {
                throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
            }
        });
    }

