    private void jbtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSaveActionPerformed
        // TODO add your handling code here:
        try {
            PorudzbinaTableModel btm = (PorudzbinaTableModel) jtblBillItems.getModel();
            BillEntity billEntity = btm.getBill();
            billEntity.setCreatedDate(LocalDate.now());
            billEntity.setCreatedTime(LocalTime.now());
            billEntity.setCreatedUser("N/A");
            if (Session.getInstance().getMap().containsKey("selected_company")) {
                billEntity.setCompanyEntity((CompanyEntity) 
                        Session.getInstance().getMap().get("selected_company")
                );
                Controller.saveBill(billEntity);
                JOptionPane.showMessageDialog(this, "Bill has been saved.");
            } else {
                throw new Exception("Please select company");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }

    }//GEN-LAST:event_jbtnSaveActionPerformed

