    private void setBillTableModel() {
        try {
            BillEntity bill = new BillEntity();
//        bill.setCreatedDate(LocalDate.now());
//        bill.setCreatedTime(LocalTime.now());

            TableModel billTableModel = new PorudzbinaTableModel(bill);
            jtblBillItems.setModel(billTableModel);

            List<ProductEntity> products = Controller.getAllProducts();
            JComboBox comboBox = new JComboBox(new DefaultComboBoxModel(products.toArray()));

            TableColumn columnProduct = jtblBillItems.getColumnModel().getColumn(1);
            columnProduct.setCellEditor(new DefaultCellEditor(comboBox));
        } catch (Exception ex) {
            Logger.getLogger(FBill.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
