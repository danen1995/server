    private void populateTableCompanies() {
        try {
            List<CompanyEntity> companies = Controller.getAllCompanies();

            TableModel tm = new KupacTableModel(companies);
            jTblCompanies.setModel(tm);
        } catch (Exception ex) {
            Logger.getLogger(FSelectCompany.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void centerForm() {
        setLocationRelativeTo(null);
    }

    private void prepareTableForSort() {
        TableRowSorter<TableModel> rowSorter = new TableRowSorter<>(jTblCompanies.getModel());
        jTblCompanies.setRowSorter(rowSorter);

        jtxtSeachCompanyName.getDocument().addDocumentListener(new DocumentListener() {

            @Override
            public void insertUpdate(javax.swing.event.DocumentEvent e) {
                String filterCity = jtxtSeachCompanyName.getText();

                if (filterCity.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + filterCity));
                }
            }

            @Override
            public void removeUpdate(javax.swing.event.DocumentEvent e) {
                String text = jtxtSeachCompanyName.getText();

                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void changedUpdate(javax.swing.event.DocumentEvent e) {
                throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
            }
        });
    }

