    private void jBtnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSelectActionPerformed
        int selectedRow = jTblCompanies.getSelectedRow();
        if (selectedRow >= 0) {
            Long companyId = (Long) jTblCompanies.getValueAt(selectedRow, 0);
            JOptionPane.showMessageDialog(null, "Selected company has ID: " + companyId);

            try {
                CompanyEntity company = Controller.findCompanyByID(companyId);
                Session.getInstance().getMap().put("selected_company", company);
                dispose();
            } catch (Exception ex) {
                Logger.getLogger(FSelectCompany.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a company!!!");
        }
    }//GEN-LAST:event_jBtnSelectActionPerformed

